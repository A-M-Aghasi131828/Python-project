def EX1_4(week):
	if week == 1:
		return "monday"
	if week == 2:
		return "tuesday"
	if week == 3:
		return "wednesday"
	if week == 4:
		return "thursday"
	if week == 5:
		return "friday"
	if week == 6:
		return "saturday"
	if week == 7:
		return "sunday"
	if week > 7 or week < 1 :
		return "please enter a number between 1 to 7"
		

		
		
		
		
		
		
		